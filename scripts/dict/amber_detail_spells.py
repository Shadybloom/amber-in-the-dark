# Распределение стоимости заклинаний
    # По правилам Dungeons & Dragons 5e, заклинания вызываются из ячеек.
    # Ячейки принадлежат к кругам силы заклинаний (от 1 до 9 круга).
    # Волшебник 3 lvl имеет 4 ячейки 1 круга и 2+1 ячейки 2 круга.
    # Волшебники 1-4 lvl составляют 94% от всех магов страны.
    # От волшебника 3 lvl ожидается:
    # - 4 часа ритуалов -- Silence, Gentle_Repose, Magic_Mouth и т.д.
    # - 4 часа заговоров (кантрипы) -- Mold_Earth, Shape_Water, Mend и т.д.
    # - 4 заклинания 1 круга -- Mage_Armor, Charm_Person, Silent_Image и т.д.
    # - 3 заклинания 2 круга -- Continual_Flame, Enhance_Ability и т.д.
    # Mold_Earth для строительства, Shape_Water для горных работ, Mend в хозяйстве.
    # Mage_Armor для опасных работ, Charm_Person для переговоров, Silent_Image реклама.
    # Continual_Flame -- освещение городов, Enhance_Ability -- ремёсла, театр, наука.
    # От кастеров ожидается 300 дней/год работы (когда от простых работяг 200 дней)

