    # Число ячеек/день (на примере 4 магов по стандартному распределению): (2 + 2 + 3 + 4) / 4 = 2.75
    # Формула цены: предыдущая_цена × ((заклинаний_год_круг_-1 / заклинаний_год_круг) / базовых_ячеек)
