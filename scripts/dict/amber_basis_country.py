        # 22 миллиона пони, 8 миллионов оленей, овец, коз, зебр, ослов, кабанчиков и прочего зверья.
        # Плотность населения -- 33 копытных/км² обжитых земель (до 100 на восточном побережье)
        # Урбанизация 37%, горожанами считаются жители 3-тысячных городков вроде Понивиля.
            # - 12% населения живут в 16 мегаполисах с 50+ тысячами жителей.
            # - 13% населения живут в 100 городах (15-50 тыс. жителей)
            # - 12% населения живут в 1200 городках (3-15 тыс. жителей)
            # - 23% населения, 8000 селений (300-3000 жителей)
            # - 20% населения, 36 000 деревень (50-300 жителей)
            # - 20% населения, 180 000 ферм и хуторов (5-50 жителей)
            # https://ru.wikisource.org/wiki/ЭСБЕ/Северо-Американские_Соединенные_Штаты
            # https://ru.wikisource.org/wiki/ЭСБЕ/Франция
        # Организация страны:
            # Страна разделена на регионы --> округа --> кантоны. Каждый город, это кантон.
            # Эквестрия -- пёстрое полотно аграрных общин. Жители которых, нередко, переезжают.
            # Кредо страны -- свобода путешествий и гарантия уровня жизни, ниже которого нельзя упасть.
            # Идеология страны -- корпоративизм. Республика с властью арбитра наподобие Франции Де Голля.
            # Бюджет страны, это единый налог на землю, плюс к тому доходы государственных монополий.
            # Прожиточный минимум (бесплатные общежития, поезда, еду и медицину) даёт государство.
            # Местные правительства должны дать большее, или жители быстро уезжают на фронтир.
            # Высокий земельный налог лишает смысла владение землёй, если её не обрабатывают.
            # Кроме конституции нет обязательных законов. Кантоны живут по своему разумению.
            # https://ru.wikipedia.org/wiki/Пятая_французская_республика#Конституционные_идеи_де_Голля
            # https://ru.wikipedia.org/wiki/Геолибертарианство#Имущественные_права
            # https://ru.wikipedia.org/wiki/Корпоративизм
            # https://ru.wikipedia.org/wiki/Дирижизм
        # Форма власти:
            # Естественные основания власти: глава союза, старейшины, народное собрание.
            # - Исполнительная власть, это власть главы союза (диктатура --> тирания --> монархия)
            # - Законодательная власть, это власть старейшин (аристократия --> олигархия --> плутократия)
            # - Судебная власть, это власть народного собрания (демократия --> охлократия --> хейрократия)
            # Усиление основания обыкновенно ведёт к его деградации, как указано стрелками выше.
            # - Державы суши (Россия, Франция, Германия) -- преобладает власть главы союза.
            # - Державы моря (Голландия, Англия, Америка) -- преобладает власть старейшин.
            # - Сетевые державы (финансисты, коминтерн) -- преобладает власть собрания.
            # Эквестрия, в данной классификации, держава суши под властью тирана.
            # https://ru.wikisource.org/wiki/ЭСБЕ/Власть
            # https://ru.wikisource.org/wiki/ЭСБЕ/Правительство
            # The Social Effects of D&D Magic
            # https://tragedyandfarce.blog/2021/10/06/the-social-effects-of-dd-magic/
        # Архитектура власти:
            # Активы страны, это банки, предприятия, месторождения, города. Точки контроля финансов.
            # Властные группы в теории элит, это товарищества с разными паями поверх структуры активов.
            # Властные группы ограничены числом Донбара (1 элитарий, 10 членов семьи, 150 шлейфа)
            # В Эквестрии это 600 элитариев, 6 000 приближённых, 100 000 шлейфа (каждая 300 пони)
            # - Каждая пони с доходом x16 модальной заработной платы с 1/3 шансом часть шлейфа.
            # - Мэра каждого из тысячи городов Эквестрии можно считать приближённым к элите.
            # - Наследственных владельцев ТОП-100 компаний можно смело отнести к элитариям.
            # Это паутина "мафиозных" семей, опутывающая финансы и бюрократию государства.
            # Эквестрия, суть тирания. Тысячелетняя богиня допускает в элиту только своих.
            # Поэтому не будет ошибкой назвать крупнейшие предприятия госмонополиями.
