        # Группы элит:
            # Государству необходимы функции: безопасность, экономика, контроль.
            # Усиление функции в ответ на дестабилизацию: война, рост, сепаратизм.
            # Эти функции порождают группы элит: силовиков, олигархов, идеологов.
            # Их власть делится как 60:30:10 (консенсус первых и 10% оппозиции)
            # ---
            # Для примера: силовики-олигархи (BRICS) и олигархи-идеологи (G7).
            # Их оппозиция: идеологи для стран BRICS и военные для стран G7.
            # Природа мятежей: эволюционное давление --> адаптация оппозиции.
            # Первой жертвой мятежа становятся слабейшие, среднее 30% звено.
            # Ситуация французской революции, олигархи поглотили идеологов.
