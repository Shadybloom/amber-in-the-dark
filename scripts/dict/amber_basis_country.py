        # Наследование:
            # Уровень деволюции наследства -- 3-3.5% (наследование в 30 лет)
            # Накопления бедняков (без ренты): 420 × 15% × 30 = 1890 бит/семью
            # Накопления бедняков: 420 × 15% × 30 + 33 × 30 = 2880 бит/семью
            # Инфляция накоплений: 2880 бит × 0.99 ^ 30 лет = 2130 бит/семью
            # Базовое наследство (в 25 лет): 1636 × 0.6 × 2 = 1963 бит/семью
            # ББН на 5% ВВП получают 100% подростков, 300 тыс пони по 1000 бит.
            # Это значит 2% налог на 50% капитала, что принадлежит 10% богатым.
            # Формула (налог с капитала): (6 × 6 × 50% × 2% × 25 = 6 × 6% × 25)
            # Формула (налог с наследства): (6 × 6 × 50% × 50% = 6 × 6% × 25)
            # Фактически это забой 880 кабанов и 60% налог с наследства прочих.
            # За 30 лет элиты страны на 20-40% обновляются путём разграбления.
