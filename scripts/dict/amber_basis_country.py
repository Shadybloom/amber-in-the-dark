            # 1500 кв.км/год, 0.2% площади/год, ×7 рост страны за 1000 лет
            # Миграция населения: 2% внутренняя (440k пони), 0.2% вовне (40k)
            # Фермеры переезжают туда, где нет "Погодного налога" и властей.
            # Базовое наследство позволяет им закупаться новейшей агротехникой.
            # - 186k земных подростков, 20% уезжают на фронтир с 40+40 млн бит.
            # - Это средства на 3800 ферм (+2.1% ферм, но не всех ждёт успех)
            # - Это 22k бит на 10 пони (дом и амбар, инвентарь, аренда земли)
            # Туземные козы не продают им землю, но сдают за часть прибыли.
            # Благодаря передовой агротехнике прибыли больше, чем у местных.
            # Земные замещают горе-земледельцев, платя им ренту (20% урожая)
            # Это подкуп туземцев едой, 5-10 рационов на кв.км (480 бит/ферма)
            # Так создаётся избыток свободной пищи, рабочих и денег на рынке.
            # Следом за фермерами движутся фабриканты, растут общины, города.
            # С "Союзным договором" приходят лесники, врачи, погодная служба.
            # Погодный налог крайне высокий, поэтому фермеры снова уезжают.
