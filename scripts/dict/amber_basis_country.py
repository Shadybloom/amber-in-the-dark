            # В Эквестрии власть силовиков-идеологов и дестабилизация роста:
            # - Силовики Эквестрии, это богиня, чиновники, врачи и погодники.
            # - Идеологи Эквестрии, это земные с их уравниловкой наследства.
            # - Олигархи Эквестрии, это ограбляемые банкиры и промышленники.
