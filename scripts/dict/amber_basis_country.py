            # Проблемы ферм: колебания трудоспособности семьи, уязвимость.
            # Проблемы общин: чересполосица, дистанции, власть стариков.
            # Общины замечательно плодовиты, но рождают уйму недовольных.
            # Земные, взбешённые уравниловкой и дальними наделами, уезжают.
            # "Очерки экономики мелкого земледелия" Маслов, 1918 год, стр 59
            # https://ru.wikisource.org/wiki/ЭСБЕ/Фермерское_хозяйство
            # https://ru.wikisource.org/wiki/ЭСБЕ/Хутор
            # https://istmat.org/node/21905
