            # Минимальная оплата труда: 0.2 бит/час, 32 бит/месяц, 320 бит/год.
            # Прожиточный минимум: 0.2 бит/день, 6 бит/месяц, всего 84 бит/год.
            # Повседневный рацион: 0.13-0.16 бит/сутки, 55-70% прож. минимума.
            # https://ru.m.wikipedia.org/wiki/Монетизация_экономики
            # https://ru.wikisource.org/wiki/ЭСБЕ/Кредитные_билеты
            # https://ru.wikisource.org/wiki/ЭСБЕ/Курс_вексельный
