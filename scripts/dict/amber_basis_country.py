            # Менеджеры, это наёмники, большой капитал дешевле обслуживать.
            # Поэтому меньшие капиталы объединяют в фонды вроде Blackrock.
            # Инфляция, это следствие управления фондами с прибылью >6.2% в год.
            # Поэтому инфляцию следует называть регрессивным подоходным налогом.
            # Расходы на менеджмент капиталов: 36 млрд × 50% × 0.3% = 54 млн/год
