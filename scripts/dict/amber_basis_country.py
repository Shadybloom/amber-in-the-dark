        # Элиты:
            # Активы, это предприятия и фонды. Точки контроля денежных потоков.
            # Элиты, это товарищества с разными паями поверх структуры активов.
            # Это паутина "мафиозных" семей параллельно бюрократии государства.
            # Это имунная система страны с высокой скоростью реакции на угрозы.
            # Кланы по числу Данбара (1 элитарий, 10 членов семьи, 125 шлейфа)
            # 600 элитариев, 6 000 приближённых, 75 000 шлейфа (каждая 300 пони)
            # - 1 элитарий на 25-50 тысяч населения страны, 1 элитарий на округ.
            # - Владельцы ТОП-100 компаний, мэры TOP-100 городов это элитарии.
            # - Мэры каждого из тысячи городков Эквестрии, это приближённые.
            # - Каждая пони с доходом >3600 бит/год с 1/3 шансом часть шлейфа.
            # Эквестрия, суть тирания. Богиня пускает во власть только своих.
            # "Лестница в небо" Сергей Щеглов
            # "Архитектура государственной власти" Андрей Школьников:
