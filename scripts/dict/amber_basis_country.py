        # Финансовая система:
            # Рост экономики = рост энергетики (корреляция 1:1 до 1973 года)
            # Рост в 0.5-1% означает ×1.7-2.7 потребления энергии за 100 лет.
            # Рост энергетики Эквестрии более 1% невозможен (нет нефти и угля)
            # Рука рынка: когда рента больше роста 90% капитала у 10% богатых.
            # ---
            # Решение №1: Кейнсианство, монополия на капитал (США Рузвельта)
            # 30% налога на прибыль сокращает доходность капитала с 5% до 3.5%
            # 60% налога на наследство сокращает доходы с ренты с 3.5% до 0.5%
            # Прогрессивный подоходный налог снижает монопольное завышение цен.
            # Кредитование снижает риски слабых звеньев за счёт системы в целом.
            # Эмиссия + переток капитала --> спрос на товары --> рост экономики
            # Итог: ПЭК-кризис, адаптация, наследство прячут в фонды (США 1970-х)
            # ---
            # Решение №1-v2: Солидаризм, базовое наследство (Томас Пикетти)
            # 3% годового налога на имущество 10% богатеев (47-75% за 25 лет)
            # 9% ВВП наследством в 25 лет (ВВП × 6 × 50% × 75% = ВВП × 9% × 25)
            # 50% бедных 10% --> 40% капитала страны и старт во взрослую жизнь.
            # Уничтожение рантье-лендлордов, поскольку рента на землю всего 4%.
            # Рассеивание капитала --> меньше прибыль фондов --> меньше рисков.
            # Итог: Офшоризация/падение доли капитала 10% богатых с 50% до 20%.
            # ---
            # Решение №2: Социализм, монополия на труд (СССР Сталина)
            # Государство распределяет ресурсы. Партия распределяет должности.
            # Солидарная система накопления, общие риски, фиксированная ставка.
            # Снижение себестоимости = снижение цен. Вся наценка в общие фонды.
            # Менеджмент суверенных фондов дешевле, повышая ренту с 6% до 7-10%
            # Целевая проектная эмиссия. Контур потребления и контур производства.
            # Итог: ПЭК-кризис, совмещение контуров, предательство элит (1980-е)
            # ---
            # "Кристалл роста" Александр Галушка; "Эпоха роста" Олег Григорьев
            # "Капитал XXI век" Томас Пикетти "Энергия и цивилизация" Вацлав Смил
            # https://ru.wikisource.org/wiki/ЭСБЕ/Политическая_экономия
        # Экономика:
            # Бывает трудоизбыточной (Европа) и трудодефицитной (СССР, США)
            # Трудодефицитная экономика означает мобилизационная экономика.
            # Крайняя степень мобилизации, это монополия государства на труд.
            # Концентрация ресурсов за счёт истончения периферийных отраслей.
            # Переток: Производство <-- Химпром <-- услуги/земледелие/стройка.
            # Экспорт капитала, строительство производств где есть рабочие.
            # Экспорт ресурсов вместо продукции. Ресурсы дешевле по труду.
            # Мобилизационный период 10-20 лет, до 30 лет (бюджет ≥45% ВВП)
            # Ядро экономики создаётся для достижения цели (победа, реванш)
            # Высокая социальная мобильность, формирование элит для цели.
            # Деградация из-за потери цели и смены поколения управленцев.
            # https://aftershock.news/?q=node/579855
            # https://aftershock.news/?q=node/580124
            # ---
            # Эквестрия, это трудодефицитная экономика без ядра мобилизации.
            # Страна состоит из городов-корпораций и капиталистической периферии.
            # Каждой корпорации свой отраслевой банк. Риски поделены между ними.
            # Доли наследства в госкорпорациях расходятся вниз до всех служащих:
            # - В начале это защищает от распухания штатов (закон Паркинсона)
            # - Во время упадка многократно ускоряет переток капитала вовне.
            # Богиня играет роль суперхищника, добивая проблемные монополии.
            # Через приватизацию богиня раздаёт базовое наследство жеребятам.
            # Переток капитала и рабочих на периферию усиливает новые города.
            # Мобильность населения провоцирует муниципалитеты к конкуренции.
            # Периферия воспитывает кадровый резерв для новых госкорпораций.
            # Эквитех, Эквистрой, ЭквиПромТоргЗебрСбыт -- в этом суть страны.
            # https://ru.wikipedia.org/wiki/Кэйрэцу
            # https://ru.m.wikipedia.org/wiki/Дирижизм
            # https://ru.m.wikipedia.org/wiki/Дистрибутизм
            # https://ru.wikipedia.org/wiki/Политическая_машина
        # Налоги:
            # Регрессивный подоходный налог (1% инфляция) -- 14 бит на семью.
            # Базовое наследство (60% наследства 10% богачей) -- 1000 бит/пони.
            # Бюджет страны, это единый поземельный налог и НДС госмонополий.
            # Бюджеты муниципалитетов, это прогрессивный подоходный налог.
            # Соотношение: 1:1:1 (поземельный:НДС:прогрессивный)
            # ---
            # Бюджет государств 19 века, это 10% ВВП (3-6% армия, 3% управление)
            # Развитые государства 21 века, это 30-45% ВВП (10-35% соц. расходы)
            # 15-20% образование-медицина, 6-13% пенсии, 1-3% социальные выплаты.
            # Развивающиеся государства, это 15% ВВП в 1970-х --> 10% ВВП в 2000-е
            # Причина в уменьшении таможенных пошлин: 5% в 1970-х --> 1% в 2000-е
            # Социальное государство = высокие налоги = система наёмного труда
            # "Капитал в XXI веке" Томас Пикетти (глава 13)
            # "Economists in public service" Fisher 1919
