        # Власть:
            # Мотивы власти (мотивация от элит для общества):
            # - Опасность (награда / наказание / враг / месть)
            # - Привычка (знание / закон / авторитет / эталон)
            # По силе воздействия: идеология, эго, награда, принуждение.
            # Управление улучшается от разнообразия управляющего воздействия.
            # ---
            # Естественные основания власти: глава союза, старейшины, народное собрание.
            # - Исполнительная власть, глава союза (диктатура --> тирания --> монархия)
            # - Законодательная власть, старейшины (аристократия --> олигархия --> плутократия)
            # - Судебная власть, народное собрание (демократия --> охлократия --> хейрократия)
            # Усиление основания обыкновенно ведёт к его деградации, как указано стрелками выше.
            # - Державы суши (Россия, Франция, Германия) -- власть главы союза силовиков
            # - Державы моря (Голландия, Англия, Америка) -- власть старейшин олигархов
            # - Сетевые державы (Евреи, Ватикан, Коминтерн) -- власть собрания идеологов
            # Эквестрия, в данной классификации, держава суши под властью тирана.
            # https://ru.wikisource.org/wiki/ЭСБЕ/Власть
