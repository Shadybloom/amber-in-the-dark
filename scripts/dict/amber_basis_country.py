            # "Архитектура государственной власти" -- Андрей Школьников:
            # https://paste.debian.net/plainh/a38c939b
        # Землевладение:
            # Высокий земельный налог (Погодный налог) сводит к нулю выгоды от спекуляции землёй.
            # Дожди управляемые, так что улучшение земли (дренирование) не повышает её качеств.
            # Удобрения весьма дороги, фосфаты обходятся дороже севооборота и отдыха земли.
            # В столь мрачных условиях не могло сложиться сословия крупных землевладельцев.
            # Собственники земли: государство, города, фабриканты, общины, мелкие фермеры.
            # 1) Государство требует огромного "Погодного налога", почти равного ренте.
            # 2) Города легко платят налог, покупают предместья у земных и расширяются.
            # 3) Фабриканты платят местным за ресурсы: месторождения, торф, строевой лес.
            # 4) Общины земных платят налог, скармливая излишки зерна местному зверью.
            # 5) Фермеры-арендаторы бегут от поземельного налога, уезжая на фронтир.
        # Землепользование:
            # Проблемы личного землевладения: колебания трудоспособности семьи.
            # Проблемы общинного земледелия: черезполосица, дистанции, власть стариков.
            # Младшие земные, недовольные уравниловкой и малыми наделами, нередко уезжают.
            # Они создают фермы там, где это дешевле. Где ещё нет "Погодной службы" и городов.
            # Туземные козы не продают им свою землю, но позволяют арендовать за часть прибыли.
            # Благодаря передовой агротехнике этой прибыли всегда больше, чем у местных.
            # Постепенно земные замещают двурогих земледельцев, а те живут с ренты.
            # "Очерки экономики мелкого земледелия" Маслов, 1918 год, стр 59
            # https://ru.wikisource.org/wiki/ЭСБЕ/Фермерское_хозяйство
            # https://ru.wikisource.org/wiki/ЭСБЕ/Хутор
            # https://istmat.org/node/21905
        # Экспансия:
            # Фермеры подкупают туземцев арендной платой, дружбой и выгодами цивилизации.
            # Следом за фермерами движутся фабриканты, переселяются общины, растут города.
            # Наконец, вместе с "союзным договором" ставит метеостанции погодная служба.
            # Лишь только так, со скоростью тысячелетней улитки, Эквестрия расширяется.
            # Поэтому рядом с 22 млн пони живут 8 миллионов присоединившихся других.
            # Поэтому законы, это конституция страны и свои обычаи на каждый кантон.
