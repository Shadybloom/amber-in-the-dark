            # Доходность капитала: 6% рента - 4% налоги = 2% (720 млн, 12% ВВП)
            # Страта    | Семей | Доля | Капитал | Капитал/семью | Рента/семью
            # --------- | ----- | ---- | ------- | ------------- | -----------
            # Кабаны    | 44k   | 1%   | 20%     | 164 000 бит   | 3275
