        # Баланс властей:
            # Эквестрийская власть строится на трёх основаниях и шести ветвях.
            # Три основания в философии утилитаризма: жизнь, счастье и свобода.
            # Три основные ветви власти противостоят ветвям критиков/сменщиков.
            # Элиты распределены поровну по ветвям власти (выравнивание элит)
            # ---
            # "Старые деньги" (чиновники, советы и врачи) -- поземельный налог.
            # - исполнительная власть, назначаемый мэр и/или избираемый шериф.
            # - законодательная власть, советы местных земледельческих кланов.
            # - судебная власть, реликтовая аристократия врачей и примирителей.
            # Врачи Эквестрии, это служебное дворянство, жрецы при теократии.
            # vs:
            # "Новые деньги" (гвардейцы, школа и пресса) -- доходы госмонополий.
            # - кризисная власть, гвардия, суть организованная преступность.
            # - экзаменационная власть, единая система школ и сбора статистики.
            # - контрольная власть, нотариат и пресса на деньги местного бизнеса.
            # Гвардия, это институциализированная преступность (якудза, триады)
            # ---
            # "Новые деньги" служат пентестерами, но не имеют ресурса созидать.
            # "Старые деньги" противостоят новым, вместо усиления своей власти.
            # Элитам нужны сменщики, иначе бюрократизация, потеря адаптивности.
            # Борьба элит, это защита от вырождения, когда нет внешнего врага.
            # Пока энергия этих утырков тратится попусту, пони живут вольнее.
            # https://ru.m.wikipedia.org/wiki/Материалистическая_диалектика#Диалектическая_триада
            # https://aurora.network/articles/10-vlast-i-obshhestvo/69179-priroda-i-protsess-neuderzhimogo-rosta-privilegiy?print
        # Землевладение:
            # Зерновые/бобы: 100-150 бит/гектар. Овощи/фрукты: 150-200 бит/га
            # - Единый поземельный налог (25-40% урожая) мешает спекуляции землёй.
            # - Базовое наследство, 1000 бит во многом заменяет фермерский кредит.
            # - Дожди управляемые, так что дренирование не повышает качеств почвы.
            # - Удобрения весьма дороги, аммиак дороже севооборота и отдыха земли.
            # В столь мрачных условиях не могло сложиться сословие лендлордов.
            # ---
            # Землевладельцы: государство, города, фабриканты, общины, фермеры.
            # - Госмонополии владеют землями при дорогах, каналах, карьерах.
            # - Города платят налог и скупают предместья у земных под застройку.
            # - Фабриканты платят налог за месторождения, торф, строевой лес.
            # - Общины земных платят налог, скармливая излишки туземному зверью.
            # - Фермеры-арендаторы бегут от земельного налога, уезжая на фронтир.
            # Если земля не обрабатывается, то строго принадлежит местным зверям.
            # https://ru.wikisource.org/wiki/ЭСБЕ/Регалии,_финансовый_термин
            # https://ru.m.wikipedia.org/wiki/Геолибертарианство#Имущественные_права
            # https://ru.m.wikipedia.org/wiki/Аграризм
        # Землепользование:
            # Селения беднее ферм из-за расстояний от усадьбы до поля:
            # - Ферма (5 дворов, 21 пони/км², 1.2 км²  r=0.6 км): 211 бит/пони
            # - Деревня (25 дворов, 25 пони/км², 5 км², r=1.3 км): 173 бит/пони
