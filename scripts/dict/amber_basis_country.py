        # Капиталы Эквестрии:
            # Норма сбережений -- 15% (40+ лет на накопление ×6 дохода)
            # Национальное состояние: ВВП × 6 = 36 млрд бит (1636 бит/пони)
            # Народное богатство | Стоимость | % ВВП | % Капитала
