    # Это разросшаяся деревня, где большинство всё ещё земледельцы.
    # Это площадь обжитых земель в 17 км², 28% обрабатываемых почв.
    # Проблема чересполосицы и расстояний до полей осложняет работы.
    # Селения на 30-50% менее производительны, чем деревни и фермы.
    # Инфраструктура развитая, вклады в землю не увеличивают доходы.
    # Имущественное расслоение чёткое, верхние 20% богаче в 4 раза.
    # Статусный разрыв заметен каждому, богатые живут отдельно.
    # Богатые переносят капиталы в город, начинают свой бизнес.
    # Социальные связи ослаблены. Селение разделено на кланы.
    # 25-50% молодёжи получая наследство едут на фронтир.
    # Варианты развития:
    # - молодёжь разъезжается, селение угасает за 30 лет (85%)
