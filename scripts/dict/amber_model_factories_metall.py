# Фабрики (капитализация)
# Строительство мартеновской фабрики:
    # Мартеновская фабрика с двумя печами (здание было рассчитано на три печи).
        # - Размеры фабрики 81×36.3 метров
        # - площадь -- около 3000 кв. метров
    # Место -- дровяная площадь в восточной части завода.
        # - грунт -- плотная красная глина.
        # - высота -- 5.3 метра над площадью завода, 
        # - высота -- 6.4 метра над горизонтом водоотводных канав.
        # - удобный наклон для подвоза дров
    # Фабрика строилась три года:
        # 1897 год:
            # - Июль -- планировка площадки
            # - 11 августа -- земляные работы, устройство фундамента под фермы.
            # - 28 сентября -- сложен последний фундамент
            # - 17 февраля -- прибыл первый вагон с частями каркаса
            # - 9 марта -- прибыли (котельщики?) для сборки и постановки ферм
            # - 15 марта -- началась работа по сборке ферм
            # - 30 марта -- поднята первая промежуточная ферма (с южного конца фабрики)
        # 1898 год:
            # - 1 июня -- готов каркас здения (с опозданием на 8 месяцев)
            # - к зиме -- закончена обшивка и окраска здания
            # - к сентябрю -- готов верхний пол (железный, на балках)
            # - зимой -- сделаны котлованы под 2 дымовые трубы
            # - 23 ноября -- начата открытая канава перед штольней для дренажа
            # - 12 декабря -- начали подземную работу по водоотводной штольне
        # 1899 год:
            # - 11 января  -- прошли 32 сажени (68 метров) от устья штольни (1/3 длины)
            # - 19 февраля -- дошли до поворота штольни
            # - весна -- готовы фундаменты под дымовые трубы
            # - 30 июня -- дошли до второго поворота, зайдя штреком к генераторам
            # - 25 июля -- наконец, подошли к зданию
            # - лето -- получены первые кирпичи от кирпичного завода в Лысьвено
            # - осень -- подняты и футлерованы обе дымовые трубы
        # 1899 год:
            # - 17 января -- печь №1 окончательно построена, начата сушка дровами
            # - 8 марта -- в печь пущен газ
            # - 11 марта -- пущена первая печь
            # - 13 мая -- пущена вторая печь
    # Планировка площадки:
        # - стоимость работ 3918 рублей (27 копеек/кубометр)
        # - вывезено 14 600 кубометров навала, мусора, шепья (5 кубометров на кв.метр площадки)
    # Фундамент:
        # - глубина фундамента -- 2.13 метра.
            # клались фундаменты на известковом растворе с прибавление портландского цемента.
            # к зиме фундаменты засыпали соломой, чтобы защитить от промерзания. Почвенная вода мешала.
    # Каркас:
        # - Стоимость -- 36 575 рублей (5.7 рубля/кв.метр здания)
        # - Вес каркаса -- 147 тонн (249 руб/тонна)
        # - 13 трёхшарнирных промежуточных ферм, на расстоянии 19 футов одна от другой (5.8 метра)
        # - поднимали каждую ферму с помощью четырёх воротов, через блоки (20-30 минут/ферма)
        # - гужевая перевозка за 25.6 км от станции Лысьва до завода.
            # Здание должно быть поставлено через 5 месяцев после заказа.
            # но только через 7 месяцев прибыл первый вагон с частями здания.
            # прибыли котельщики, инженер, техник, опытный монтёр.
            # Перед подъёмомо обе половины ферм соединялись средним шарнирным болтом.
            # Против проекта фирмы добавлена диагональная связь в плоскости крыши.
            # Соседние фермы соединялись балками.
            # После подъёма первой фермы инженер уехал, оставив только техника.
            # К концу рабочего дня все десять ферм упали.
                # Фермы лишь погнулись, исправляли домкратами.
                # расходы фирмы-установщика 2000 рублей.
                # Каркас здания от фирмы Бари:
                # http://moskva-yug.ucoz.ru/publ/2-1-0-82
    # Фасады
        # - 6 колонн.
    # Крыша:
        # - Кровельное железо -- 15.8 тонн, 5 кг/кв.метр
    # Обшивка стен:
        # - Квадратные бруски (111×111-мм) между фермами. Такие же поперечины и раскосы.
        # - Всё это зашито изнутри кровельным железом.
    # Окна:
        # - С одной стороны фабрики сплошная панель стеклянных рам.
        # - Со стороны печей такая же панель (но в два света)
        # - В концевых фасадах по 7 отдельных окон.
    # Пол:
        # - верхний жёсткий (железный) пол. Стоил 0.21 рубля/килограмм
            # кроме стоимости привоза из Нижнего Новгорода
        # - железные прокатные двутавровые балки (380 мм вышиной)
            # проложены в три ряда вдоль печей, на чугунные колонки.
        # - чугунные колонки крестообразного сечения (358.6 кг/штука стоят на 5.7 одна от другой)
            # стоят на чугунных плитках, уложенных на каменных столбах.
