        # Сталь (Япония, 1800 год) -- ~385 ГДж первичной энергии (20 кг дерева --> 5 кг угля --> 1 кг стали)
        # Чугун (США, 1820 год) -- ~300 ГДж первичной энергии (20 кг дерева --> 5 кг угля --> 1 кг чугуна)
        # Чугун (США, 1910 год) -- ~72 ГДж первичной энергии (5 кг дерева --> 1.2 кг угля --> 1 кг чугуна)
