# Фабрики (чёрная металлургия)
    # Чугун (95-97% железо, 1.8-4% углерод и 0.5-3% кремний)
        # Углеродистая сталь (углерод 0.3-0.95%) -- 90% мирового производства
        # Нержавеющая сталь (18% хром, 8% никель) -- дороже чёрной в 3.6 раза
    # Детали техпроцессов:
        # Известняк во флюсе избавляет от серы. Сера портит стальной прокат.
        # Ферромарганец (сплав марганца и железа) в шихте решает проблему фосфора.
        # Кокс нужен для восстановления железа и как структура шихты для выдувания.
        # Содержание серы в коксе до 7-15% (>20% непригоден), влажность до 2-3%
        # 10% зольность -- 0.5-0.55 тонны кокса на тонну чугуна
        # 20% зольность -- 0.8-0.9 тонна кокса на тонну чугуна
